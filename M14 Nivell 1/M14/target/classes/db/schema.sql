
DROP TABLE IF EXISTS SHOP;
DROP TABLE IF EXISTS PICTURE;

CREATE TABLE SHOP(
	ID INT PRIMARY KEY AUTO_INCREMENT,
	NAME VARCHAR(255),
	CAPACITAT_MAXIMA INT(4)
);

CREATE TABLE PICTURE(
	ID INT PRIMARY KEY AUTO_INCREMENT,
	NAME VARCHAR(255),
	PREU INT(6),
	DATA_ENTRADA DATE,
	SHOP_ID INT NOT NULL,
	FOREIGN KEY (SHOP_ID) REFERENCES SHOP(ID)
);
